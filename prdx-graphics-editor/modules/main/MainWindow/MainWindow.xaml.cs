    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            Color? color = Actions.PickColor();
            if (color != null)
            {
                (sender as Button).Background = new SolidColorBrush(color.GetValueOrDefault());
            }
        }
    }
